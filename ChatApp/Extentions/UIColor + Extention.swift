import UIKit

extension UIColor {
    
    static func buttonRed() -> UIColor {
        return .red
    }
    
    static func buttonWhite() -> UIColor {
        return .white
    }
    
    static func buttonBlack() -> UIColor {
           return .darkGray
       }
}
