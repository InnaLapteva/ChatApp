import UIKit

extension UIColor {
    
    static func buttonRed() -> UIColor {
        return #colorLiteral(red: 0.5807225108, green: 0.066734083, blue: 0, alpha: 1)
    }
    
    static func buttonWhite() -> UIColor {
        return #colorLiteral(red: 0.9999960065, green: 1, blue: 1, alpha: 1)
    }
    
    static func buttonBlack() -> UIColor {
           return #colorLiteral(red: 0.2605174184, green: 0.2605243921, blue: 0.260520637, alpha: 1)
       }
    
    static func textFieldLight() -> UIColor {
              return #colorLiteral(red: 0.5741485357, green: 0.5741624236, blue: 0.574154973, alpha: 1)
          }
}
